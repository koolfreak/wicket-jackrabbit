public class DMSException extends Exception
{

    public DMSException()
    {
	super();
    }

    public DMSException(String message, Throwable cause)
    {
	super(message, cause);
    }

    public DMSException(String message)
    {
	super(message);
    }

    public DMSException(Throwable cause)
    {
	super(cause);
    }

    
}
